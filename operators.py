"""
Operadores Aritméticos:


+ = Addition
- = Subtraction
* = Multiplication
/ = Division
% = Modulus
** = Exponentiation
// = Floor Division


"""

num1 = 10
num2 = 3
result = num1 + num2
print("Soma:", result)


"""
Operadores de Comparação:

== = Equal to
!= = Not equal to
> = Greater than
< = Less than
>= = Greater than or equal to
<= = Less than or equal to

"""

num1 = 10
num2 = 5

print("num1 == num2:", num1 == num2)
print("num1 != num2:", num1 != num2)
print("num1 > num2:", num1 > num2)
print("num1 < num2:", num1 < num2)
print("num1 >= num2:", num1 >= num2)
print("num1 <= num2:", num1 <= num2)



"""
Operadores Lógicos:

and = Logical AND
or = Logical OR
not = Logical NOT

"""

num1 = 10
num2 = 5

num1 > 3 and num2 < 10  # True
num1 > 9 or num2 > 10  # True

not (num1 > 5)  # False
